//位运算符
#include <stdio.h>
int main(void) {
    //位与
    //0x5a:0101 1010
    //0xe3:1110 0011
    //&-------------
    //0x42:0100 0010
    printf("0x5a & 0xe3的结果是%#x\n", 0x5a & 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //&-------------
    //0x40:0100 0000
    printf("0x44 & 0xc1的结果是%#x\n", 0x44 & 0xc1);
    //位或
    //0x5a:0101 1010
    //0xe3:1110 0011
    //|-------------
    //0xfb:1111 1011
    printf("0x5a | 0xe3的结果是%#x\n", 0x5a | 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //|-------------
    //0xc5:1100 0101
    printf("0x44 | 0xc1的结果是%#x\n", 0x44 | 0xc1);
    //位异或:相同为0,不同为1
    //0x5a:0101 1010
    //0xe3:1110 0011
    //^-------------
    //0xb9:1011 1001
    printf("0x5a ^ 0xe3的结果是%#x\n", 0x5a ^ 0xe3);
    //0x44:0100 0100
    //0xc1:1100 0001
    //^-------------
    printf("0x44 ^ 0xc1的结果是%#x\n", 0x44 ^ 0xc1);
    //位反
    //0x5a默认的类型是int类型,32位,4字节
    //0x5a完整的2进制:0000 0000 0000 0000 0000 0101 1010
    //取反:           1111 1111 1111 1111 1111 1010 0101
    //                  f   f    f    f     f    a   5
    //%#x:32位,4输出
    printf("%#x\n", ~0x5a);//0101 1010->1010 0101 = 0xa5
    printf("%#hhx\n", ~0x5a);//0101 1010->1010 0101 = 0xa5
    return 0;
}

